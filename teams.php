    <h3>New Build Teams</h3>
    <p>If you decide to form a new build team, your team will need the following:
    <ol>
        <li>A team leader, who will be the team's contact for the student leadership committee and will manage major
            team decisions
        </li>
        <li>An adult build team representative (parent of a team member) who will help handle fund management and be the
            adult contact for the leadership committee
        </li>
        <li>All members having filled out the registration, be on the mailing list, and pay the club dues</li>
        <li>Buy parts for your new robot</li>
